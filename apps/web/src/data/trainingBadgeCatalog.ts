function toI18nSuffix(id: string): string {
  return id.replace(/_([a-z0-9])/g, (_, letter: string) => letter.toUpperCase());
}

export const trainingBadgeCatalog: readonly TrainingBadgeDefinition[] = BADGE_SEEDS.map(
  (seed) => {
    const suffix = toI18nSuffix(seed.id);

    return {
      ...seed,
      requiredScore: seed.requiredScore ?? 80,
      requiredCompletionRate: 100,
      i18nKey: `badges.${suffix}`,
      descriptionKey: `badges.${suffix}.description`,
      unlockHintKey: `badges.${suffix}.unlock`,
    };
  },
);
